def run_curfew_alert(sheet_manager, mastodon_client)
  unless auto_push_enabled?(sheet_manager, "통금알람")
    puts "[스킵] 통금알람이 OFF 상태입니다."
    return
  end

  message = CURFEW_MESSAGES.sample
  mastodon_client.broadcast(message)
end

